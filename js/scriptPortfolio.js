// Script for Portfolio (open tabs and reveals them)

// 1
// Blocco uscita 
let block = document.body.getElementsByClassName("cont2")[0];

// Evento apertura 
block.onclick = function() {
    document.body.getElementsByClassName("tab")[0].style.display = "block"
}
// Blocco entrata 
let close = document.body.getElementsByClassName("tab2")[0].lastElementChild;

// Evento chiusura 
close.onclick = function() {
    document.body.getElementsByClassName("tab")[0].style.display = "none";
}

